public class Main {

    /**
     * Checks if a given age is old enough to drive.
     * @param age: The age to check
     * @returns True if the year is old enough to drive(>=16), otherwise false.
     *
     *
     * @author <Amadou Taal>
     * @email <s-amadou.taal@lwtech.edu>
     */
    public static boolean CanDrive(int age) {

        int drivingAge = 16;
        return age >= drivingAge;

    }
}
